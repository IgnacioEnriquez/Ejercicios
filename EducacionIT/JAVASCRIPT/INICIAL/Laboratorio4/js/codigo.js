var date_obj = new Date();
var dia = date_obj.getDay();

switch (dia) {

  case 0:
    alert("Es un hermoso Domingo de Verano!(Ojala)");
    break;

  case 1:
    alert("Es un hermoso Lunes de Verano!(Ojala)");
    break;

  case 2:
    alert("Es un hermoso Martes de Verano!(Ojala)");
    break;

  case 3:
    alert("Es un hermoso Miercoles de Verano!(Ojala)");
    break;

  case 4:
    alert("Es un hermoso Jueves de Verano!(Ojala)");
    break;
    
  case 5:
    alert("Es un hermoso Viernes de Verano!(Ojala)");
    break;

  case 6:
    alert("Es un hermoso Sabado de Verano!(Ojala)");
    break;    
}
